package com.shopper.entity;

public class album {
	private int aid;
	private int pid;
	private String plmg;
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getPlmg() {
		return plmg;
	}
	public void setPlmg(String plmg) {
		this.plmg = plmg;
	}
}
